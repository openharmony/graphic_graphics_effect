def normalize_type(type_str: str, type_aliases: Dict[str, str]) -> str:
    """Normalize a type string using type aliases and removing unnecessary spaces.

    Spaces are removed except:
    - Between type qualifiers (const, volatile, mutable) and next token
    - Between type modifiers (unsigned, signed, short, long) and next token
    - After commas inside template parameters
    """
    # First apply type aliases
    type_str = type_aliases.get(type_str, type_str)

    # Keywords that should preserve space after them
    preserve_space_keywords = {"const", "volatile", "mutable", "unsigned", "signed", "short", "long"}

    # Tokenize type string, preserving structure
    # We need to track template depth to know when we're inside <...>
    tokens = []
    depth = 0
    i = 0
    while i < len(type_str):
        ch = type_str[i]

        # Track template depth
        if ch == "<":
            depth += 1
            tokens.append(ch)
            i += 1
        elif ch == ">":
            depth = max(0, depth - 1)
            tokens.append(ch)
            i += 1
        elif ch == ",":
            # Commas are preserved inside templates
            tokens.append(ch)
            i += 1
        elif ch in [" ", "\t", "\n"]:
            # Collect all whitespace
            ws = ch
            i += 1
            while i < len(type_str) and type_str[i] in [" ", "\t", "\n"]:
                ws += type_str[i]
                i += 1
            tokens.append(("ws", ws))
        elif ch in ["*", "&"]:
            tokens.append(ch)
            i += 1
        elif ch == ":" and i + 1 < len(type_str) and type_str[i + 1] == ":":
            tokens.append("::")
            i += 2
        else:
            # Collect identifier/keyword
            ident = ch
            i += 1
            while i < len(type_str) and (type_str[i].isalnum() or type_str[i] == "_"):
                ident += type_str[i]
                i += 1
            tokens.append(ident)

    # Build normalized string
    result = []
    i = 0
    while i < len(tokens):
        token = tokens[i]

        # Skip whitespace
        if isinstance(token, tuple) and token[0] == "ws":
            i += 1
            continue

        # Add token
        result.append(token)

        # Check if we need to preserve space after this token
        if token == ",":
            # Add a space after comma (for template parameters)
            result.append(" ")
        elif token in preserve_space_keywords:
            # Look ahead to see if there's a non-whitespace token
            j = i + 1
            while j < len(tokens):
                next_token = tokens[j]
                if isinstance(next_token, tuple) and next_token[0] == "ws":
                    j += 1
                    continue
                break

            if j < len(tokens) and tokens[j] not in ["<", ">", "::", "*", "&", ","]:
                # Add a space
                result.append(" ")

        i += 1

    return "".join(result)


def parse_def_file(file_path: Path, report_errors: bool = True) -> Optional[StructInfo]:
    """Parse a .params file and extract struct information."""
    with open(file_path, "r", encoding="utf-8") as f:
        content = f.read()

    # Tokenize
    tokenizer = CppTokenizer(content)
    tokens = tokenizer.tokenize()

    # Parse
    parser = CppParser(tokens, str(file_path))
    structs = parser.parse()

    # Report errors if requested
    if report_errors and parser.errors:
        for error in parser.errors:
            console.error(f"  {error}")

    # Return the first (and typically only) struct
    if structs:
        return structs[0]
    return None


def scan_declare_gefilter_typefunc(effect_dirs: List[Path]) -> List[FilterTypeMacroInfo]:
    """Scan effect directories for DECLARE_GEFILTER_TYPEFUNC macro usage."""
    macro_infos = []

    for effect_dir in effect_dirs:
        # Find all .h files in the directory
        header_files = list(effect_dir.glob("*.h"))
        for header_file in header_files:
            try:
                with open(header_file, "r", encoding="utf-8") as f:
                    content = f.read()

                # Pattern to match DECLARE_GEFILTER_TYPEFUNC(Class, ParamsType)
                pattern = r"DECLARE_GEFILTER_TYPEFUNC\s*\(\s*([a-zA-Z_][a-zA-Z0-9_]*)\s*,\s*([a-zA-Z_][a-zA-Z0-9_:<>:]*)\s*\)"

                matches = re.finditer(pattern, content)
                for match in matches:
                    filter_class = match.group(1)
                    params_type = match.group(2)

                    # Get relative path from include directory
                    # effect_dir is like: .../include/effect/filter
                    # We want: effect/filter/xxx.h
                    # header_file is like: .../include/effect/filter/xxx.h
                    # We need to get the path relative to include/
                    include_dir = header_file.parent.parent.parent  # .../include
                    rel_path = header_file.relative_to(include_dir)
                    header_include = str(rel_path)

                    macro_infos.append(FilterTypeMacroInfo(filter_class=filter_class, params_type=params_type, header_file=header_include))
            except Exception as e:
                console.warning(f"Failed to scan {header_file}: {e}")

    return macro_infos


def to_upper_case(name: str) -> str:
    """Convert camelCase or snake_case to UPPER_CASE."""
    if name.endswith("_"):
        name = name[:-1]
    result = []
    for c in name:
        if c.isupper():
            if result:
                result.append("_")
            result.append(c)
        else:
            result.append(c.upper())
    return "".join(result)


def to_pascal_case(name: str) -> str:
    """Convert snake_case or camelCase to PascalCase.

    Examples:
        propName -> PropName
        prop_name -> PropName
        _propName -> PropName (strip leading underscore)
    """
    # Remove trailing underscore
    if name.endswith("_"):
        name = name[:-1]
    # Remove leading underscore
    if name.startswith("_"):
        name = name[1:]

    if not name:
        return name

    # Convert to PascalCase
    result = []
    capitalize_next = True
    for c in name:
        if c == "_":
            capitalize_next = True
        elif capitalize_next:
            result.append(c.upper())
            capitalize_next = False
        else:
            result.append(c)
    return "".join(result)


def get_effective_type_for_tag(
    field: FieldInfo, is_array_elem: bool, array_idx: Optional[int], prop_attr_index: int, type_aliases: Dict[str, str]
) -> str:
    """Get the effective type for a field tag.

    This function consolidates the logic for determining the effective type
    that was duplicated in generate_set_params_member_overloads_impl() and collect_effective_types().

    Args:
        field: The field info
        is_array_elem: Whether this is an array element tag
        array_idx: The array index (if is_array_element is True)
        prop_attr_index: The index of the prop attribute to use for cast_from
        type_aliases: Type alias mapping

    Returns:
        The normalized effective type string
    """
    cast_from_type = None
    if field.prop_attributes and prop_attr_index < len(field.prop_attributes):
        cast_from_type = field.prop_attributes[prop_attr_index].cast_from

    if cast_from_type:
        effective_type = cast_from_type
    elif is_array_elem and field.prop_attributes:
        for prop_attr in field.prop_attributes:
            if prop_attr.array_accessor_length is not None:
                if prop_attr.array_accessor_type:
                    effective_type = prop_attr.array_accessor_type
                else:
                    effective_type = field.type
                break
    else:
        effective_type = field.type

    return normalize_type(effective_type, type_aliases)


