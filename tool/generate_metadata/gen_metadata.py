def generate_constraint_metadata(field_info: FieldInfo, tag: str, prop_attr_index: int = 0) -> str:
    """Generate constraint metadata for a field with prop attributes."""
    if not field_info.prop_attributes:
        return ""

    field_type = field_info.type
    output = []

    # Use the specified prop attribute for constraint metadata
    # (prop_attr_index indicates which prop attribute to use)
    prop_attr = field_info.prop_attributes[prop_attr_index]

    # Handle min constraint
    if prop_attr.min_value is not None:
        output.append(generate_min_constraint(tag, field_type, prop_attr.min_value))

    # Handle max constraint
    if prop_attr.max_value is not None:
        output.append(generate_max_constraint(tag, field_type, prop_attr.max_value))

    # Handle convert constraints (cast_from/custom)
    if prop_attr.cast_from is not None:
        output.append(generate_convert_constraint(tag, prop_attr.cast_from, prop_attr.custom))

    return "\n".join(output)


