def generate_constraints(structs: List[StructInfo]):
    """Generate constraint macros and metadata"""
    output = []
    # Generate constraint specialization macros
    output.append(CONSTRAINT_MACROS)

    # Generate constraint metadata for constrained fields
    for struct in structs:
        for field in struct.fields:
            for tag_info in iterate_field_tags(struct, field):
                metadata = generate_constraint_metadata(field, tag_info.tag_name, tag_info.prop_attr_index)
                if metadata:
                    output.append(metadata)

    output.append(CONSTRAINT_MACRO_UNDEF)
    return "\n".join(output)


def generate_header(structs: List[StructInfo], type_aliases: Dict[str, str], blocked_types: Dict[str, str] = None) -> str:
    """Generate the complete header file."""
    if blocked_types is None:
        blocked_types = {}

    output = []

    output.append(COPYRIGHT_HEADER)
    output.append("")
    output.append("#ifndef GRAPHICS_EFFECT_GE_PARAMS_REFLECTION_H")
    output.append("#define GRAPHICS_EFFECT_GE_PARAMS_REFLECTION_H")
    output.append("")
    output.append("#include <algorithm>")
    output.append("#include <cmath>")
    output.append("#include <cstdint>")
    output.append("#include <optional>")
    output.append("#include <string>")
    output.append("#include <string_view>")
    output.append("#include <type_traits>")
    output.append("#include <utility>")
    output.append("")
    output.append('#include "ge_effects_params.h"')
    output.append('#include "ge_value_transformer_traits.h"')
    output.append('#include "ge_value_transformer.h"')
    output.append("")

    output.append("namespace OHOS {")
    output.append("namespace Rosen {")
    output.append("namespace Drawing {")
    output.append("")
    output.append("class GEFilterParams;")
    output.append("")

    output.append("// Helper macro to escape commas in macro arguments")
    output.append("// e.g. CALL(std::pair<int, int>), the comma would separate into two macro arguments")
    output.append("//      CALL(ESCAPE(std::pair<int, int>)) treats it as one argument")
    output.append("// Used to pass types as-is without extra parentheses which can cause issues in some contexts")
    output.append("#define ESCAPE(...) __VA_ARGS__")
    output.append("")

    output.append(generate_type_xmacro(structs, type_aliases, blocked_types))

    # Generate member enums first (before base templates, so no forward declaration needed)
    enum_code, struct_tag_ranges = generate_member_enums(structs)
    output.append(enum_code)

    # Generate base constraint templates (after enum, so GEParamsMemberTag is defined)
    output.append(BASE_CONSTRAINT_TEMPLATES)

    # Generate GEParamsValueTransformer template
    output.append(VALUE_TRANSFORMER_TEMPLATE)

    # Generate constraint specializations
    output.append(generate_constraints(structs))

    # Generate GEFilterParamsTypeInfo specializations
    output.append(generate_filter_params_type_info(structs))
    output.append(generate_params_builder_decl())

    # Generate type traits
    output.append(generate_type_traits(structs))

    # Generate GEParamsMemberHelper declaration (includes SetParamsMemberByTag template)
    output.append(generate_struct_tag_ranges_decl(structs, type_aliases, blocked_types))

    output.append("} // namespace Drawing")
    output.append("} // namespace Rosen")
    output.append("} // namespace OHOS")
    output.append("")
    output.append("#endif // GRAPHICS_EFFECT_GE_PARAMS_REFLECTION_H")

    return "\n".join(output)


def generate_cpp(structs: List[StructInfo], type_aliases: Dict[str, str], blocked_types: Dict[str, str] = None) -> str:
    """Generate complete cpp file."""
    if blocked_types is None:
        blocked_types = {}

    output = []

    output.append(COPYRIGHT_HEADER)
